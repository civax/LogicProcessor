    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextArea3.getLineCount()<3){
            JOptionPane.showMessageDialog(this, "deben proporcionarse al menos 2 argumentos y una conclusion, separados por salto de linea", "Número de argumentos inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        LinkedList<String> expresiones=new LinkedList<>();
        for(String element:Arrays.asList(jTextArea3.getText().split("\n"))){
            expresiones.add(element);
        }
        String expresion="("+expresiones.pollFirst()+")";
        String aux="("+expresiones.pollLast()+")";
        while(!expresiones.isEmpty()){
            expresion+="^("+expresiones.pollFirst()+")";
        }
        expresion+=Operador.$CONDITIONAL+aux;
        GeneradordeTabla generador=new GeneradordeTabla(expresion);
        generador.evaluarExpresion();
        if(generador.esTautologia()){
            jLabel13.setText("Los argumentos dados son válidos.");
            jLabel13.setForeground(Color.DARK_GRAY);
            jTextArea4.setText(generador.getFormatedTable());
        }else {
            jLabel13.setText("Los argumentos dados son inválidos.");
            jLabel13.setForeground(Color.red);
            jTextArea4.setText(generador.getFormateTableWithFalseEvaluations());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

