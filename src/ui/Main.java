        generador.evaluarExpresion();
        jTextArea2.setText(generador.getFormatedTable());
        if(generador.esTautologia()){
            this.label.setText("La expresión dada es una Tautología.");
            this.label.setForeground(Color.DARK_GRAY);
        }else if(generador.esContradiccion()){
            this.label.setText("La expresión dada es una Contradicción.");
            this.label.setForeground(Color.red);
        }else{
            this.label.setText("La expression dada es una Contingencia.");
            this.label.setForeground(Color.red);
        }
