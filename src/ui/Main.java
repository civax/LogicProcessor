    private TreeSet<String> getUniverso(){
        List<List<String>> lista=new ArrayList<>();
            if(jTextField1.getText().length()>0){
                lista.add(Arrays.asList(jTextField1.getText().split(",")));
            }
            if(jTextField4.getText().length()>0){
                lista.add(Arrays.asList(jTextField4.getText().split(",")));
            }
            if(jTextField5.getText().length()>0){
                lista.add(Arrays.asList(jTextField5.getText().split(",")));
            }
            if(jTextField6.getText().length()>0){
                lista.add(Arrays.asList(jTextField6.getText().split(",")));
            }
            if(jTextField7.getText().length()>0){
                lista.add(Arrays.asList(jTextField7.getText().split(",")));
            }
        return OperadorDeConjuntos.getInstance().getUnion(lista);
    }
