    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String expresion=jTextField3.getText();
        expresion=Expresion.normalizarTexto(expresion);
        String subExpresion=jTextField3.getSelectedText();
        subExpresion=Expresion.normalizarTexto(subExpresion);
        String equivalencia=jComboBox1.getSelectedItem().toString();
        Expresion exp=new Expresion(subExpresion);
        System.out.println("[LOG]Aplicando equivalencia logica");
        System.out.println("[LOG]Expresion inicial: "+expresion);
        System.out.println("[LOG]SubExpresion inicial: "+subExpresion);
        if(!Objects.isNull(subExpresion)){
            String prefix=expresion.substring(0, expresion.indexOf(subExpresion));
            System.out.println("[LOG]Prefijo: "+prefix);
            String sufix=expresion.substring(expresion.indexOf(subExpresion)+subExpresion.length(),expresion.length());
            System.out.println("[LOG]sub Expresion: "+subExpresion);
            System.out.println("[LOG]Sufijo: "+sufix);
            System.out.println("[LOG]Aplicando  "+equivalencia+" a "+subExpresion);
            try{
                subExpresion=exp.convertir(equivalencia, subExpresion);
                if(contador==1){
                    jTextArea5.setText(contador+". "+expresion+"\tExpresion inicial");
                    contador++;
                }
                jTextArea5.setText(
                    jTextArea5.getText()+"\n"+
                    contador+". "+prefix+subExpresion+sufix+"\t"+equivalencia
                );
            }catch(IllegalArgumentException e){
                JOptionPane.showMessageDialog(this, e.getMessage(),"ERROR", JOptionPane.ERROR_MESSAGE);
            }
            System.out.println("[LOG]Resultado  "+subExpresion);
            jTextField3.setText(prefix+subExpresion+sufix);
        }else{
            JOptionPane.showMessageDialog(this, "Favor de seleccionar una subexpresion","Regla de inferencia", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

